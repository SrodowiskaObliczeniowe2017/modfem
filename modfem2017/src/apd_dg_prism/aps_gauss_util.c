/*--------------------------------------------------------------------
  apr_gauss_select - to select the proper quadrature
--------------------------------------------------------------------*/
void apr_gauss_select(
	int Type,	/* in: type of integration considered */
			/* 	1 - LINE INTEGRATION over [-1,1] */
			/* 	2 - GAUSSIAN QUADRATURES FOR TRIANGLES */
	int Order, 	/* in: order of integration == degree */
			/*     of polynomials integrated exactly */
        int *Ng,	/* out: number of gauss points */
	double **Xg_p, 	/* out: gauss points */
	double **Wg_p 	/* out: gauss weights */
	)
{
  /* auxiliary variables */
  int i,j;

/*++++++++++++++++ executable statements ++++++++++++++++*/

  if (Type == 1) {
    /* LINE INTEGRATION over [-1,1] */
    
    *Ng=Order/2+1;
    
    if(*Ng<=12){
      *Xg_p = Xg_1d[*Ng - 1];
      *Wg_p = Wg_1d[*Ng - 1];
    }
    else {
      printf("not implemented yet, number of gauss points too big!\n");
      exit(-1);
    }
  } /* Ltype = 1 */
  else if(Type==2){
    /* GAUSSIAN QUADRATURES FOR TRIANGLES */
    
    /* change order of integration for quadratures with points */
    /* outside of an element */
    if(Order==11){
      Order=12;
    }
    else if(Order==15){
      Order=17;
    }
    else if(Order==16){
      Order=17;
    }
    else if(Order==18){
      Order=19;
    }
    
    if(Order==1 || Order==0){
      *Ng= 1;
    }
    else if(Order== 2){
      *Ng= 3;
    }
    else if(Order== 3){
      *Ng= 4;
    }
    else if(Order== 4){
      *Ng= 6;
    }
    else if(Order== 5){
      *Ng= 7;
    }
    else if(Order== 6){
      *Ng=12;
    }
    else if(Order== 7){
      *Ng=13;
    }
    else if(Order== 8){
      *Ng=16;
    }
    else if(Order== 9){
      *Ng=19;     
    }
    else if(Order==10){
      *Ng=25;            
    }
    else if(Order==11){
      *Ng=27;      
    }
    else if(Order==12){
      *Ng=33;
    }
    else if(Order==13){
      *Ng=37;
    }
    else if(Order==14){
      *Ng=42;
    }
    else if(Order==15){
      *Ng=48;
    }
    else if(Order==16){
      *Ng=52;
    }
    else if(Order==17){
      *Ng=61;
    }
    else if(Order==18){
      *Ng=70;
    }
    else if(Order==19){
      *Ng=73;
    }

    if(Order<=0){
      *Xg_p = Xg_2d[0];
      *Wg_p = Wg_2d[0];
    }
    else if(Order<=19){
      *Xg_p = Xg_2d[Order - 1];
      *Wg_p = Wg_2d[Order - 1];
    }
    else {
      printf("not implemented yet, number of gauss points too big!\n");
      exit(-1);
    }
  }
  
  return;
}

